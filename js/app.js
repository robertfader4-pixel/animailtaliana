const STORAGE_KEY = 'italian-mood-state-v1';

function formatTime(time) {
  if (!Number.isFinite(time)) return '0:00';
  const minutes = Math.floor(time / 60);
  const seconds = Math.floor(time % 60);
  return `${minutes}:${String(seconds).padStart(2, '0')}`;
}

function saveState() {
  const payload = {
    currentIndex: state.currentIndex,
    currentTime: els.audio.currentTime || 0,
    volume: els.audio.volume,
    isPlaying: !els.audio.paused,
    playerPosition: {
      left: els.floatingPlayer.style.left || '',
      top: els.floatingPlayer.style.top || '',
      right: els.floatingPlayer.style.right || '',
      bottom: els.floatingPlayer.style.bottom || ''
    },
    playerMinimized: els.floatingPlayer.classList.contains('minimized')
  };
  localStorage.setItem(STORAGE_KEY, JSON.stringify(payload));
}

function loadState() {
  try {
    const saved = JSON.parse(localStorage.getItem(STORAGE_KEY) || 'null');
    if (!saved) return;
    if (typeof saved.currentIndex === 'number' && saved.currentIndex >= 0 && saved.currentIndex < tracks.length) {
      state.currentIndex = saved.currentIndex;
    }
    if (typeof saved.volume === 'number') {
      els.audio.volume = saved.volume;
      els.volumeBar.value = saved.volume;
    }
    if (saved.playerPosition) {
      Object.assign(els.floatingPlayer.style, saved.playerPosition);
    }
    if (saved.playerMinimized) {
      els.floatingPlayer.classList.add('minimized');
      els.expandPlayerBtn.classList.add('visible');
    }
    return saved;
  } catch {
    return null;
  }
}

