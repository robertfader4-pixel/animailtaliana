function ensureAudioContext() {
  if (state.audioCtx) return;
  const AudioContext = window.AudioContext || window.webkitAudioContext;
  if (!AudioContext) return;
  state.audioCtx = new AudioContext();
  state.analyser = state.audioCtx.createAnalyser();
  state.analyser.fftSize = 256;
  state.analyser.smoothingTimeConstant = 0.86;
  state.dataArray = new Uint8Array(state.analyser.frequencyBinCount);
  state.sourceNode = state.audioCtx.createMediaElementSource(els.audio);
  state.sourceNode.connect(state.analyser);
  state.analyser.connect(state.audioCtx.destination);
  drawSpectrum();
}

function drawSpectrum() {
  const canvas = els.spectrumCanvas;
  const ctx = canvas.getContext('2d');
  const resize = () => {
    canvas.width = window.innerWidth * window.devicePixelRatio;
    canvas.height = document.querySelector('.hero').offsetHeight * window.devicePixelRatio;
    canvas.style.height = `${document.querySelector('.hero').offsetHeight}px`;
    ctx.setTransform(window.devicePixelRatio, 0, 0, window.devicePixelRatio, 0, 0);
  };
  resize();
  window.addEventListener('resize', resize);

  const render = () => {
    const width = canvas.width / window.devicePixelRatio;
    const height = canvas.height / window.devicePixelRatio;
    ctx.clearRect(0, 0, width, height);

    if (state.analyser) {
      state.analyser.getByteFrequencyData(state.dataArray);
    }

    const baseline = height * 0.72;
    const step = width / (state.dataArray?.length || 64);
    const gradient = ctx.createLinearGradient(0, baseline - 120, width, baseline + 40);
    gradient.addColorStop(0, 'rgba(255,255,255,0.0)');
    gradient.addColorStop(0.15, `${hexToRgba(getComputedStyle(document.documentElement).getPropertyValue('--accent').trim(), 0.1)}`);
    gradient.addColorStop(0.5, `${hexToRgba(getComputedStyle(document.documentElement).getPropertyValue('--accent').trim(), 0.52)}`);
    gradient.addColorStop(1, `${hexToRgba(getComputedStyle(document.documentElement).getPropertyValue('--accent-2').trim(), 0.08)}`);

    ctx.beginPath();
    ctx.moveTo(0, baseline);

    const usableData = state.dataArray || new Uint8Array(64).fill(0);
    for (let i = 0; i < usableData.length; i += 1) {
      const value = usableData[i] / 255;
      const wave = Math.sin((i / usableData.length) * Math.PI * 4 + performance.now() * 0.0014) * 10;
      const y = baseline - value * 150 - wave;
      const x = i * step;
      ctx.lineTo(x, y);
    }

    for (let i = usableData.length - 1; i >= 0; i -= 1) {
      const value = usableData[i] / 255;
      const wave = Math.sin((i / usableData.length) * Math.PI * 4 + performance.now() * 0.0014) * 6;
      const y = baseline + value * 52 + wave;
      const x = i * step;
      ctx.lineTo(x, y);
    }

    ctx.closePath();
    ctx.fillStyle = gradient;
    ctx.shadowColor = getComputedStyle(document.documentElement).getPropertyValue('--accent').trim();
    ctx.shadowBlur = 28;
    ctx.fill();
    ctx.shadowBlur = 0;

    ctx.beginPath();
    ctx.moveTo(0, baseline - 2);
    for (let i = 0; i < usableData.length; i += 1) {
      const value = usableData[i] / 255;
      const wave = Math.sin((i / usableData.length) * Math.PI * 4 + performance.now() * 0.0014) * 8;
      const y = baseline - value * 110 - wave;
      const x = i * step;
      ctx.lineTo(x, y);
    }
    ctx.strokeStyle = `${hexToRgba(getComputedStyle(document.documentElement).getPropertyValue('--accent').trim(), 0.95)}`;
    ctx.lineWidth = 2.2;
    ctx.stroke();

    state.rafId = requestAnimationFrame(render);
  };
  render();
}

function hexToRgba(hex, alpha = 1) {
  if (!hex || !hex.startsWith('#')) return `rgba(255,255,255,${alpha})`;
  const clean = hex.replace('#', '');
  const chunks = clean.length === 3
    ? clean.split('').map((char) => char + char)
    : [clean.slice(0,2), clean.slice(2,4), clean.slice(4,6)];
  const [r,g,b] = chunks.map((part) => parseInt(part, 16));
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}

function syncPlayButtons() {
  const paused = els.audio.paused;
  els.playBtn.textContent = paused ? '▶' : '❚❚';
  els.heroPlayBtn.textContent = paused ? 'Слушать сейчас' : 'Пауза';
}

function playAudio() {
  ensureAudioContext();
  if (state.audioCtx?.state === 'suspended') state.audioCtx.resume();
  els.audio.play().then(() => {
    state.isPlaying = true;
    syncPlayButtons();
    saveState();
  }).catch(() => {});
}

function pauseAudio() {
  els.audio.pause();
  state.isPlaying = false;
  syncPlayButtons();
  saveState();
}

function fadeToAndPlay(newIndex) {
  clearInterval(state.fadeInterval);
  const originalVolume = parseFloat(els.volumeBar.value);
  let workingVolume = els.audio.volume;
  const step = Math.max(originalVolume / 10, 0.04);

  if (els.audio.paused || els.audio.currentTime === 0) {
    loadTrack(newIndex, true);
    return;
  }

  state.fadeInterval = setInterval(() => {
    workingVolume -= step;
    if (workingVolume <= 0.02) {
      clearInterval(state.fadeInterval);
      els.audio.volume = 0;
      loadTrack(newIndex, true, () => {
        let fadeInVolume = 0;
        els.audio.volume = 0;
        state.fadeInterval = setInterval(() => {
          fadeInVolume += step;
          els.audio.volume = Math.min(fadeInVolume, originalVolume);
          if (fadeInVolume >= originalVolume) {
            clearInterval(state.fadeInterval);
          }
        }, 70);
      });
    } else {
      els.audio.volume = workingVolume;
    }
  }, 65);
}

function loadTrack(index, autoplay = false, callback = null) {
  state.currentIndex = (index + tracks.length) % tracks.length;
  const track = tracks[state.currentIndex];
  els.audio.src = track.file;
  els.audio.load();
  setMeta(track);
  updatePlaylistActiveState();
  syncPlayButtons();
  if (autoplay) {
    playAudio();
  } else {
    pauseAudio();
  }
  if (typeof callback === 'function') callback();
  saveState();
}

function togglePlayback() {
  if (els.audio.paused) playAudio();
  else pauseAudio();
}

function seekToPercentage(percent) {
  if (!Number.isFinite(els.audio.duration) || els.audio.duration <= 0) return;
  els.audio.currentTime = (percent / 100) * els.audio.duration;
}

function applyRestoredTrack(saved) {
  const track = tracks[state.currentIndex];
  els.heroImageA.style.backgroundImage = `url('${track.image}')`;
  els.currentCover.src = track.image;
  setMeta(track);
  els.audio.src = track.file;
  els.audio.load();
  els.audio.addEventListener('loadedmetadata', () => {
    if (saved?.currentTime && saved.currentTime < els.audio.duration - 1) {
      els.audio.currentTime = saved.currentTime;
    }
    if (saved?.isPlaying) playAudio();
    saveState();
  }, { once: true });
}

function setupEvents() {
  els.playBtn.addEventListener('click', togglePlayback);
  els.heroPlayBtn.addEventListener('click', togglePlayback);
  els.prevBtn.addEventListener('click', () => fadeToAndPlay(state.currentIndex - 1));
  els.nextBtn.addEventListener('click', () => fadeToAndPlay(state.currentIndex + 1));

  els.audio.addEventListener('timeupdate', () => {
    if (Number.isFinite(els.audio.duration) && els.audio.duration > 0) {
      const progress = (els.audio.currentTime / els.audio.duration) * 100;
      els.progressBar.value = progress;
      els.currentTime.textContent = formatTime(els.audio.currentTime);
      els.duration.textContent = formatTime(els.audio.duration);
      saveState();
    }
  });

  els.audio.addEventListener('ended', () => fadeToAndPlay(state.currentIndex + 1));
  els.audio.addEventListener('play', syncPlayButtons);
  els.audio.addEventListener('pause', syncPlayButtons);

  els.progressBar.addEventListener('input', (e) => seekToPercentage(Number(e.target.value)));
  els.volumeBar.addEventListener('input', (e) => {
    els.audio.volume = Number(e.target.value);
    saveState();
  });

  els.focusToggle.addEventListener('click', () => {
    document.body.classList.toggle('focus-mode');
    els.focusToggle.textContent = document.body.classList.contains('focus-mode') ? 'Exit focus' : 'Focus mode';
  });

  els.lyricsModeBtn.addEventListener('click', () => {
    document.getElementById('lyricsSection').scrollIntoView({ behavior: 'smooth', block: 'start' });
    document.body.classList.add('focus-mode');
    els.focusToggle.textContent = 'Exit focus';
  });

  els.minimizeBtn.addEventListener('click', () => {
    els.floatingPlayer.classList.add('minimized');
    els.expandPlayerBtn.classList.add('visible');
    saveState();
  });

  els.expandPlayerBtn.addEventListener('click', () => {
    els.floatingPlayer.classList.remove('minimized');
    els.expandPlayerBtn.classList.remove('visible');
    saveState();
  });

  const startDrag = (clientX, clientY) => {
    const rect = els.floatingPlayer.getBoundingClientRect();
    state.isDraggingPlayer = true;
    state.dragOffsetX = clientX - rect.left;
    state.dragOffsetY = clientY - rect.top;
    els.floatingPlayer.style.right = 'auto';
    els.floatingPlayer.style.bottom = 'auto';
    els.floatingPlayer.style.left = `${rect.left}px`;
    els.floatingPlayer.style.top = `${rect.top}px`;
  };

  const onMove = (clientX, clientY) => {
    if (!state.isDraggingPlayer) return;
    const nextLeft = Math.min(window.innerWidth - els.floatingPlayer.offsetWidth - 8, Math.max(8, clientX - state.dragOffsetX));
    const nextTop = Math.min(window.innerHeight - els.floatingPlayer.offsetHeight - 8, Math.max(8, clientY - state.dragOffsetY));
    els.floatingPlayer.style.left = `${nextLeft}px`;
    els.floatingPlayer.style.top = `${nextTop}px`;
  };

  els.dragHandle.addEventListener('mousedown', (e) => startDrag(e.clientX, e.clientY));
  els.dragHandle.addEventListener('touchstart', (e) => {
    const touch = e.touches[0];
    startDrag(touch.clientX, touch.clientY);
  }, { passive: true });

  window.addEventListener('mousemove', (e) => onMove(e.clientX, e.clientY));
  window.addEventListener('touchmove', (e) => {
    if (!state.isDraggingPlayer) return;
    const touch = e.touches[0];
    onMove(touch.clientX, touch.clientY);
  }, { passive: true });

  window.addEventListener('mouseup', () => {
    if (state.isDraggingPlayer) saveState();
    state.isDraggingPlayer = false;
  });
  window.addEventListener('touchend', () => {
    if (state.isDraggingPlayer) saveState();
    state.isDraggingPlayer = false;
  });

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) entry.target.classList.add('visible');
    });
  }, { threshold: 0.12 });

  document.querySelectorAll('.reveal').forEach((node) => observer.observe(node));
}

function init() {
  renderPlaylist();
  renderDurations();
  const saved = loadState();
  els.audio.volume = Number(els.volumeBar.value);
  applyRestoredTrack(saved);
  setupEvents();
  syncPlayButtons();
}

init();
